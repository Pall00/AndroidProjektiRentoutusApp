package com.example.androidprojectirentoutusapp;

public class User {
    private int age;
    private int weight;
    private int height;
    private String name;
    private int level;

    public User(int age, int weight, int height, String name, int level) {
        this.age = age;
        this.weight = weight;
        this.height = height;
        this.name = name;
        this.level = level;
    }
}
